#include <stdio.h>

int main()
{
    int n = 1;

    printf("1 AND 1 = %d\n", n&n);  /* 00000001 & 00000001 = 00000001  */
    printf("1 OR  1 = %d\n", n|n);  /* 00000001 | 00000001 = 00000001  */
    printf("1 XOR 1 = %d\n", n^n);  /* 00000001 ^ 00000001 = 00000000  */
    printf("  NOT 1 = %d\n",  ~n);  /* ~00000001 = 110---snip---       */
    printf("1 SHL 1 = %d\n", n<<n); /* 00000001 << 00000001 = 00000010 */
    printf("1 SHR 1 = %d\n", n>>n); /* 00000001 >> 00000001 = 00000000 */

    return(0);
}
